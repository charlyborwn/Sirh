    private void tablacivilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablacivilMouseClicked
        DefaultTableModel modelo= (DefaultTableModel) tablacivil.getModel();
        estado.setText(modelo.getValueAt(tablacivil.getSelectedRow(), 0)+"");
        fecha.setText(modelo.getValueAt(tablacivil.getSelectedRow(), 2)+"");
        usuario.setText(modelo.getValueAt(tablacivil.getSelectedRow(), 3)+"");
        crear.setEnabled(false);
        estado.setEnabled(false);
    }//GEN-LAST:event_tablacivilMouseClicked

