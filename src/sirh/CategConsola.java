    private void crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActionPerformed
        Categ e=new Categ();
        String desca=categoria.getText().toUpperCase();
        if(desca.isEmpty()){
            JOptionPane.showMessageDialog(this, "Se requiere una clave de categoria, verifique .....");
            Iniciar();
        }
        else{
            String desce=cveempresa.getSelectedItem().toString();
            String desem=empresa.getText().toUpperCase();
            String desco=codigo.getText().toUpperCase();
            Float dessu=Float.parseFloat(sueldo.getText());
            String desde=descripcion.getText().toUpperCase();
            e.Agregar(desca, desce, desem, desco, dessu, desde, Acceso.getClaveUsuario());
            JOptionPane.showMessageDialog(this, "Registro creado .....");
            Iniciar();  
        }  
    }                                     

