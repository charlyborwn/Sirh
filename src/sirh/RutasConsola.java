    private void crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActionPerformed
        Rutas e=new Rutas();
        String dnumrut = numrut.getText().toUpperCase();
        if(dnumrut.isEmpty()){
            JOptionPane.showMessageDialog(this, "Se requiere un nÃºmero o clave de ruta (1), verifique .....");
            numrut.grabFocus();
        }
        else{
            if(dnumrut.trim().equals("")){
                JOptionPane.showMessageDialog(this, "Se requiere un nÃºmero o clave de ruta (2), verifique .....");
                numrut.setText("");
                numrut.grabFocus();
            }
            else{
                String dcveemp = cveemp.getSelectedItem().toString();
                if(dcveemp.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Se requiere una clave de empresa, verifique .....");
                    cveemp.grabFocus(); 
                }
                else{
                    String dnomemp=nomemp.getText().toUpperCase();
                    String dlugori=lugori.getText().toUpperCase();
                    String dlugdes=lugdes.getText().toUpperCase();
                    String dpath=path.getText().toUpperCase();
                    String dobserva=observa.getText().toUpperCase();
                    e.Agregar(dnumrut, dcveemp, dnomemp, dlugori, dlugdes, dpath, dobserva, Acceso.getClaveUsuario());
                    JOptionPane.showMessageDialog(this, "Registro creado .....");
                    Iniciar();
                }
            }
        }
    }//GEN-LAST:event_crearActionPerformed

