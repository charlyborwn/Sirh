    private void ingresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ingresarMouseClicked

        String Usuario = usuario.getText().toUpperCase();
        String Contra = new String(contrase単a.getPassword()).toUpperCase();
        
        if(Usuario.isEmpty() || Contra.isEmpty()){
            JOptionPane.showMessageDialog(this, "Faltan datos, verifique .....");
            usuario.setText("");
            contrase単a.setText("");
            usuario.grabFocus();
        }
        else{
            Acceso acc= new Acceso();
            Object[] datos=acc.Datos(Usuario, Contra);
            cvusu.setText(datos[0]+"");
            nousu.setText(datos[1]+"");
            neusu.setText(datos[2]+"");
            grusu.setText(datos[3]+"");
            nausu.setText(datos[4]+"");
            
            if(cvusu.getText().trim().equals("null")){
                JOptionPane.showMessageDialog(this, "Acceso Denegado .....");
                System.exit(0);
            }
            else{
                try {
                    Acceso.setClaveUsuario(cvusu.getText());
                    Acceso.setNombreUsuario(nousu.getText());
                    Acceso.setEmpresaUsuario(neusu.getText());
                    Acceso.setGrupoUsuario(grusu.getText());
                    Acceso.setNivelUsuario(nausu.getText());
                    Menu object=new Menu();
                    object.setVisible(true); 
                    this.dispose();
                } catch (MalformedURLException ex) {
                    Logger.getLogger(AccesoConsola.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_ingresarMouseClicked

