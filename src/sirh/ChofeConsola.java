    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        Chofe e=new Chofe();
        String drfc=rfc.getText().toUpperCase();
        if(drfc.isEmpty()){
            JOptionPane.showMessageDialog(this, "Se requiere un Rfc, verifique .....");
            rfc.grabFocus();
        }
        else{
            if(fecnac.getDate() == null){
                JOptionPane.showMessageDialog(this, "Se requiere una fecha de nacimiento, verifique .....");
                fecnac.grabFocus();
            }
            else{
                String dcurp=curp.getText().toUpperCase();
                String dpaterno=paterno.getText().toUpperCase();
                String dmaterno=materno.getText().toUpperCase();
                String dnombre=nombre.getText().toUpperCase();
                String dsexo=sexo.getSelectedItem().toString();
                int año=fecnac.getCalendar().get(Calendar.YEAR);
                int mes=fecnac.getCalendar().get(Calendar.MONTH)+1;
                int dia=fecnac.getCalendar().get(Calendar.DAY_OF_MONTH);
                String dnacim=(año+"/"+mes+"/"+dia);
                String dgrado=maxim.getText().toUpperCase();
                String dcivil=civil.getSelectedItem().toString();
                String dpais=pais.getSelectedItem().toString();
                String destado=estado.getSelectedItem().toString();
                String dmundel=mundel.getSelectedItem().toString();
                String dpobla=pobla.getSelectedItem().toString();
                String dcolonia=colonia.getSelectedItem().toString();
                String dcalle=calle.getText().toUpperCase();
                String dnumext=numext.getText().toUpperCase();
                String dnumint=numint.getText().toUpperCase();
                int dcodpos=Integer.parseInt(codpos.getText().toUpperCase());
                String dteldom=teldom.getText().toUpperCase();
                String dtelcel=telcel.getText().toUpperCase();
                String dcorreo=correo.getText().toUpperCase();
                e.Actualizar(drfc, dcurp, dpaterno, dmaterno, dnombre, dsexo, dnacim, dgrado, dcivil, dpais, destado, dmundel, dpobla,
                             dcolonia, dcalle, dnumext, dnumint, dcodpos, dteldom, dtelcel, dcorreo, Acceso.getClaveUsuario());
                JOptionPane.showMessageDialog(this, "Registro actualizado .....");
                Iniciar();
            }
        }
    }//GEN-LAST:event_actualizarActionPerformed

