    private void tablachofeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablachofeMouseClicked
        DefaultTableModel modelo= (DefaultTableModel) tablachofe.getModel();
        rfc.setText(modelo.getValueAt(tablachofe.getSelectedRow(), 0)+"");
        curp.setText(modelo.getValueAt(tablachofe.getSelectedRow(), 1)+"");
        paterno.setText(modelo.getValueAt(tablachofe.getSelectedRow(), 2)+"");
        materno.setText(modelo.getValueAt(tablachofe.getSelectedRow(), 3)+"");
        nombre.setText(modelo.getValueAt(tablachofe.getSelectedRow(), 4)+"");
        fecha.setText(modelo.getValueAt(tablachofe.getSelectedRow(), 6)+"");
        usuario.setText(modelo.getValueAt(tablachofe.getSelectedRow(), 7)+"");
        crear.setEnabled(false);
        rfc.setEnabled(false);
        
        String RegFed = rfc.getText();
        String PaAc = paterno.getText();
        String MaAc = materno.getText();
        String NoAc = nombre.getText();
        
        Chofe com=new Chofe();
        Object[] datos=com.Comple(RegFed, PaAc, MaAc, NoAc);
        sexo.setSelectedItem(datos[0]+"");
        String fnacim = datos[1]+"";
        SimpleDateFormat formatofecha=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        try{
            Date fecha1 = formatofecha.parse(fnacim);
            fecnac.setDate(fecha1);
        }
        catch(ParseException pe1){
            System.out.println("Error Parse1: "+pe1.getMessage());
        }
        maxim.setText(datos[2]+"");
        civil.setSelectedItem(datos[3]+"");
        pais.setSelectedItem(datos[4]+"");
        estado.setSelectedItem(datos[5]+"");
        mundel.setSelectedItem(datos[6]+"");
        pobla.setSelectedItem(datos[7]+"");
        colonia.setSelectedItem(datos[8]+"");
        calle.setText(datos[9]+"");
        numext.setText(datos[10]+"");
        numint.setText(datos[11]+"");
        codpos.setText(datos[12]+"");
        teldom.setText(datos[13]+"");
        telcel.setText(datos[14]+"");
        correo.setText(datos[15]+"");
    }//GEN-LAST:event_tablachofeMouseClicked

