    private void ejecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ejecutarActionPerformed
        String NumRfc = rfc.getSelectedItem()+"";
        String NumTra = numtra.getText();
        if(fecini.getDate() == null){
            JOptionPane.showMessageDialog(this, "Faltan datos (1), verifique .....");
                Iniciar();
        }
        else{
            if("".equals(NumRfc) || "".equals(NumTra)){
                JOptionPane.showMessageDialog(this, "Faltan datos (2), verifique .....");
                Iniciar();
            }
            else{
                int Ntra = Integer.parseInt(numtra.getText());
                Convierte ver= new Convierte();
                Object[] datos1=ver.Verifica(Ntra);
                String Chk =datos1[0]+"";
                
                if(Chk.trim().equals("null")){
                    JOptionPane.showMessageDialog(this, "Inicio del Proceso de Conversión, espere .....");
                    int año=fecini.getCalendar().get(Calendar.YEAR);
                    int mes=fecini.getCalendar().get(Calendar.MONTH)+1;
                    int dia=fecini.getCalendar().get(Calendar.DAY_OF_MONTH);
                    String FecIng=(año+"/"+mes+"/"+dia);
                    String Ahora = fechaActual();
                    Convierte camtra=new Convierte();
                    camtra.CambTraba(Ntra, NumRfc, FecIng, Ahora, Acceso.getClaveUsuario());
                    Convierte camdat=new Convierte();
                    camdat.CambDatos(Ntra, NumRfc, Ahora, Acceso.getClaveUsuario());
                    Convierte camben=new Convierte();
                    camben.CambBenef(Ntra, NumRfc, Ahora, Acceso.getClaveUsuario());
                    Convierte camdoc=new Convierte();
                    camdoc.CambDocum(Ntra, NumRfc, Ahora, Acceso.getClaveUsuario());
                    Convierte camesc=new Convierte();
                    camesc.CambEscol(Ntra, NumRfc, Ahora, Acceso.getClaveUsuario());
                    Convierte camexp=new Convierte();
                    camexp.CambExper(Ntra, NumRfc, Ahora, Acceso.getClaveUsuario());
                    Convierte camfam=new Convierte();
                    camfam.CambFamil(Ntra, NumRfc, Ahora, Acceso.getClaveUsuario());
                    Convierte bortra=new Convierte();
                    bortra.BorrTraba(NumRfc);
                    Convierte bordat=new Convierte();
                    bordat.BorrDatos(NumRfc);
                    Convierte borben=new Convierte();
                    borben.BorrBenef(NumRfc);
                    Convierte bordoc=new Convierte();
                    bordoc.BorrDocum(NumRfc);
                    Convierte boresc=new Convierte();
                    boresc.BorrEscol(NumRfc);
                    Convierte borexp=new Convierte();
                    borexp.BorrExper(NumRfc);
                    Convierte borfam=new Convierte();
                    borfam.BorrFamil(NumRfc);
                    JOptionPane.showMessageDialog(this, "Proceso Concluido .....");
                }
                else{
                    JOptionPane.showMessageDialog(this, "Este numero de trabajador ya existe, verifique .....");
                    numtra.grabFocus();
                }
            }
        }
    }//GEN-LAST:event_ejecutarActionPerformed

    private void rfcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rfcActionPerformed
        String claveseleccionada=rfc.getSelectedItem()+"";
        Convierte e=new Convierte();
        Object[] datos=e.Datos(claveseleccionada);
        nombre.setText(datos[0]+"");
        curp.setText(datos[1]+"");
        fecnac.setText(datos[2]+"");
        sexo.setText(datos[3]+"");
        nacion.setText(datos[4]+"");
        path.setText(datos[5]+"");
        String lugarImagen="";
        lugarImagen=path.getText();
        Image icon=new ImageIcon(lugarImagen).getImage().getScaledInstance(txtImagen.getWidth(), txtImagen.getWidth(), Image.SCALE_DEFAULT);
        Icon imagen=new ImageIcon(icon);
        txtImagen.setIcon(imagen);
        numtra.grabFocus();
    }//GEN-LAST:event_rfcActionPerformed

