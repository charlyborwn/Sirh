    private void tablaeventMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaeventMouseClicked
        DefaultTableModel modelo= (DefaultTableModel) tablaevent.getModel();
        numeve.setText(modelo.getValueAt(tablaevent.getSelectedRow(), 0)+"");
        nomemp.setText(modelo.getValueAt(tablaevent.getSelectedRow(), 1)+"");
        String feve = (modelo.getValueAt(tablaevent.getSelectedRow(), 2)+"");
        if("null".equals(feve)){
            System.out.println("La fecha del evento esta vacia .....");
        }
        else{
            SimpleDateFormat formatofecha1=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            try{
                Date fecha1 = formatofecha1.parse(feve);
                feceve.setDate(fecha1);
            }
            catch(ParseException pe1){
            System.out.println("Error Parse1: "+pe1.getMessage());
            }
        }
        horeve.setText(modelo.getValueAt(tablaevent.getSelectedRow(), 3)+"");
        numuni.setSelectedItem(modelo.getValueAt(tablaevent.getSelectedRow(), 4)+"");
        numrut.setSelectedItem(modelo.getValueAt(tablaevent.getSelectedRow(), 5)+"");
        deseve.setText(modelo.getValueAt(tablaevent.getSelectedRow(), 6)+"");
        fecha.setText(modelo.getValueAt(tablaevent.getSelectedRow(), 8)+"");
        usuario.setText(modelo.getValueAt(tablaevent.getSelectedRow(), 9)+"");
        crear.setEnabled(false);
        cveemp.setEnabled(false);
        
        int NumAct = Integer.parseInt(numeve.getText());
        
        Event com=new Event();
        Object[] datos=com.Comple(NumAct);
        cveemp.setSelectedItem(datos[0]+"");
        maruni.setText(datos[1]+"");
        moduni.setText(datos[2]+"");
        plauni.setText(datos[3]+"");
        orirut.setText(datos[4]+"");
        desrut.setText(datos[5]+"");
        rfccho.setSelectedItem(datos[6]+"");
        nomcho.setText(datos[7]+"");
        celcho.setText(datos[8]+"");
        reseve.setText(datos[9]+"");
    }//GEN-LAST:event_tablaeventMouseClicked

