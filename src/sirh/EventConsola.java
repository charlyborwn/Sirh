    private void crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActionPerformed
        Event e=new Event();
        String dcveemp = cveemp.getSelectedItem().toString();
        if(dcveemp.isEmpty()){
            JOptionPane.showMessageDialog(this, "Se requiere una clave de empresa, verifique .....");
            cveemp.grabFocus();
        }
        else{
            if(feceve.getDate() == null){
                JOptionPane.showMessageDialog(this, "Se requiere la fecha del evento, verifique .....");
                feceve.grabFocus();
            }
            else{
                String dnumuni = numuni.getSelectedItem().toString();
                if(dnumuni.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Se requiere un número o clave de unidad, verifique .....");
                    numuni.grabFocus();
                }
                else{
                    String dnumrut = numrut.getSelectedItem().toString();
                    if(dnumrut.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Se requiere un número o clave de ruta, verifique .....");
                        numrut.grabFocus();
                    }
                    else{
                        String drfccho = rfccho.getSelectedItem().toString();
                        if(drfccho.isEmpty()){
                            JOptionPane.showMessageDialog(this, "Se requiere el rfc del chofer, verifique .....");
                            rfccho.grabFocus();
                        }
                        else{
                            String dnomemp = nomemp.getText().toUpperCase();
                            int añoe=feceve.getCalendar().get(Calendar.YEAR);
                            int mese=feceve.getCalendar().get(Calendar.MONTH)+1;
                            int diae=feceve.getCalendar().get(Calendar.DAY_OF_MONTH);
                            String dfeceve=(añoe+"/"+mese+"/"+diae);
                            String dhoreve = horeve.getText().toUpperCase();
                            String dmaruni = maruni.getText().toUpperCase();
                            String dmoduni = moduni.getText().toUpperCase();
                            String dplauni = plauni.getText().toUpperCase();
                            String dorirut = orirut.getText().toUpperCase();
                            String ddesrut = desrut.getText().toUpperCase();
                            String dnomcho = nomcho.getText().toUpperCase();
                            String dcelcho = celcho.getText().toUpperCase();
                            String ddeseve = deseve.getText().toUpperCase();
                            String dreseve = reseve.getText().toUpperCase();
                            e.Agregar(dcveemp, dnomemp, dfeceve, dhoreve, dnumuni, dmaruni, dmoduni, dplauni, dnumrut, dorirut, ddesrut,
                                      drfccho, dnomcho, dcelcho, ddeseve, dreseve, Acceso.getClaveUsuario());
                            JOptionPane.showMessageDialog(this, "Registro creado .....");
                            Iniciar();
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_crearActionPerformed

