    private void tablaubicaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaubicaMouseClicked
        DefaultTableModel modelo= (DefaultTableModel) tablaubica.getModel();
        pais.setText(modelo.getValueAt(tablaubica.getSelectedRow(), 0)+"");
        estado.setText(modelo.getValueAt(tablaubica.getSelectedRow(), 1)+"");
        municipio.setText(modelo.getValueAt(tablaubica.getSelectedRow(), 2)+"");
        poblacion.setText(modelo.getValueAt(tablaubica.getSelectedRow(), 3)+"");
        colonia.setText(modelo.getValueAt(tablaubica.getSelectedRow(), 4)+"");
        fecha.setText(modelo.getValueAt(tablaubica.getSelectedRow(), 6)+"");
        usuario.setText(modelo.getValueAt(tablaubica.getSelectedRow(), 7)+"");
        crear.setEnabled(false);
        pais.setEnabled(false);
        estado.setEnabled(false);
        municipio.setEnabled(false);
        poblacion.setEnabled(false);
        colonia.setEnabled(false);
    }//GEN-LAST:event_tablaubicaMouseClicked

