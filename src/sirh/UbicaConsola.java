    private void crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActionPerformed
        Ubica e=new Ubica();
        String despa=pais.getText().toUpperCase();
        String deses=estado.getText().toUpperCase();
        String desmo=municipio.getText().toUpperCase();
        String despo=poblacion.getText().toUpperCase();
        String desco=colonia.getText().toUpperCase();
        e.Agregar(despa, deses, desmo, despo, desco, Acceso.getClaveUsuario());
        JOptionPane.showMessageDialog(this, "Registro creado .....");
        Iniciar();
    }//GEN-LAST:event_crearActionPerformed

